<template>
  <g>
    <ellipse v-bind:class="[{filled: hasLibraries}, position]" :cx="cx" :cy="cy" :rx="rx" :ry="ry" v-on:click="showModal" >
      <title v-if="hasLibraries" v-text="tooltip"></title>
    </ellipse>
    <foreignObject>
      <WellModal ref="modal" class="modal" @alert="alert" :position="position"></WellModal>
    </foreignObject>
  </g>  
</template>

<script>

import { createNamespacedHelpers } from 'vuex'
const { mapGetters } = createNamespacedHelpers('traction/pacbio/runs')
import WellModal from '@/components/WellModal'

export default {
  name: 'Well',
  props: {
    row: {
      type: String,
      required: true
    },
    column: {
      type: String,
      required: true
    },
    cx: {
      type: String,
      required: true
    },
    cy: {
      type: String,
      required: true
    },
    rx: {
      type: String,
      required: true
    },
    ry: {
      type: String,
      required: true
    }
  },
  data () {
    return {
    }
  },
  methods: {
    alert (message, type) {
      this.$emit('alert', message, type)
    },
    showModal () {
      this.$refs.modal.showModalForPosition()
    }
  },
  computed: {
    ...mapGetters([
      'currentRun',
      'well'
    ]),
    position () {
      return `${this.row}${this.column}`
    },
    tooltip () {
      let well = this.well(this.position)
      return well.libraries.map(l =>  l.barcode).join(',')
    },
    hasLibraries () {
      let well = this.well(this.position)
      if (well === undefined) return false
      return well.libraries.length > 0
    }
  },
  mounted() {
  },
  components: {
    WellModal
  }
}
</script>

